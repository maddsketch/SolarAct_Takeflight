public enum AchievementConditionType
{
    Flag,           // unlocks when a specific story flag is set
    LevelComplete,  // unlocks when a specific level ID is completed
    KillCount,      // unlocks when totalKills reaches threshold
    PlayerLevel,    // unlocks when playerLevel reaches threshold
    Currency,       // unlocks when totalCurrency reaches threshold
    QuestComplete,  // unlocks when a specific quest is completed
}
